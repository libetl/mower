package org.toilelibre.libe.mower.moves;

public class Point {

    private final int x;
    private final int y;

    public Point (final int x, final int y) {
        super ();
        this.x = x;
        this.y = y;
    }

    public int getX () {
        return this.x;
    }

    public int getY () {
        return this.y;
    }

}
